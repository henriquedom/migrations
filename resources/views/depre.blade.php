          <h1 class="display-4 font-italic">Um problema comum</h1>
          <p class="lead ">A depressão é um problema de saúde muito comum: de acordo com relatório global lançado pela Organização Mundial da Saúde (OMS), 
            em dez anos os casos aumentaram mais de 18%, num índice que supera 320 milhões de pessoas diagnosticadas. 
            No caso do Brasil, estima-se que 5,8% da população seja afetada, sendo a maior taxa do continente latino-americano. 
            Um episódio depressivo pode ser categorizado como leve, moderado ou grave, a depender da intensidade dos sintomas. 
            Alguns dos indícios da doença são: ansiedade, alterações do apetite, 
            de peso e do sono, dor crônica, agitação ou lentificação motora, fadiga ou perda de energia, 
            sentimentos de inutilidade ou de culpa, dificuldade de concentraçãoe de tomar decisões, e nos casos mais graves, 
            pensamentos de morte e ideação suicida. Além desses itens descritos, que devem estar presentes por pelo menos duas semanas, 
            o médico também precisa considerar a história de vida do paciente ao realizar o diagnóstico. 
            No entanto, apesar de parecer um quadro único quando vemos a depressão dessa forma, 
            existem tipos diferentes que possuem sintomas e impactos diversos no dia a dia.</p>
