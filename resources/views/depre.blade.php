    <p class="lead">
     <div class="mx-sm-5 bg-warning">   A depressão é um transtorno afetivo que se caracteriza não só por uma tristeza intensa e de longa duração. 
        Estima-se que aproximadamente 350 milhões de pessoas sejam atingidas por diferentes tipos de depressão em todo o mundo.
        Altamente prevalente acomete cerca de 3% a 5% da população geral, 5% a 10% na população clínica de pacientes de ambulatório e,
        9 a 16% de pacientes internados.  
        Quando passamos por momentos difíceis em nossas vidas, é comum sentirmos tristeza, desânimo ou mau humor. 
        Porém, após alguns dias esses sentimos são contornados e conseguimos tocar a vida adiante. Na depressão isso não acontece.
        O indivíduo passa mais de duas semanas seguidas em sofrimento, caminhando para um estado de profundo vazio e comportamentos destrutivos.
        Como resultado, o dia a dia de uma pessoa com depressão fica comprometido, uma vez que a doença passa a interferir em sua capacidade de trabalhar, estudar, comer, dormir e realizar outras atividades comuns do cotidiano.
        Isso porque os sinais e sintomas da depressão incluem modificações no sono, 
        na fala, nas capacidades de pensamento, memória, raciocínio lógico, na organização emocional e, em muitas outras.<br>
      
    
